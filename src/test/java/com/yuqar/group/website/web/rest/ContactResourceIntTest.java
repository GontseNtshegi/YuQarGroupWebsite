    @Test
    @Transactional
    public void updateContact() throws Exception {
        // Initialize the database
        contactService.save(contact);

        int databaseSizeBeforeUpdate = contactRepository.findAll().size();

        // Update the contact
        Contact updatedContact = contactRepository.findOne(contact.getId());
        updatedContact
            .name(UPDATED_NAME)
            .email(UPDATED_EMAIL)
            .contact(UPDATED_CONTACT)
            .message(UPDATED_MESSAGE)
            .created_date(UPDATED_CREATED_DATE)
            .opened(UPDATED_OPENED);

        restContactMockMvc.perform(put("/api/contacts")
            .contentType(TestUtil.APPLICATION_JSON_UTF8)
            .content(TestUtil.convertObjectToJsonBytes(updatedContact)))
            .andExpect(status().isOk());

        // Validate the Contact in the database
        List<Contact> contactList = contactRepository.findAll();
        assertThat(contactList).hasSize(databaseSizeBeforeUpdate);
        Contact testContact = contactList.get(contactList.size() - 1);
        assertThat(testContact.getName()).isEqualTo(UPDATED_NAME);
        assertThat(testContact.getEmail()).isEqualTo(UPDATED_EMAIL);
        assertThat(testContact.getContact()).isEqualTo(UPDATED_CONTACT);
        assertThat(testContact.getMessage()).isEqualTo(UPDATED_MESSAGE);
        assertThat(testContact.getCreated_date()).isEqualTo(UPDATED_CREATED_DATE);
        assertThat(testContact.isOpened()).isEqualTo(UPDATED_OPENED);

        // Validate the Contact in Elasticsearch
        Contact contactEs = contactSearchRepository.findOne(testContact.getId());
        assertThat(contactEs).isEqualToComparingFieldByField(testContact);
    }

